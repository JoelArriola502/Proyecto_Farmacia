public void EliminarUsuario(){
    try {
        Eliminar=ConectarBD.prepareStatement("Delete from Usuario where idUsuario='"+txtID.getText()+"'");
        int Contador=Eliminar.executeUpdate();
        if(Contador>0){
            JOptionPane.showMessageDialog(null,"USUARIO ELIMINADO CON EXITO");
            VerDatosUsuario();
        }else{
            JOptionPane.showMessageDialog(null,"Seleccione Fila");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null,"Error al eliminar"+ e.toString());
    }
}
public void ActualizarDatos(){
      int indice =conCargo.getSelectedIndex();
        int estado=conEstado.getSelectedIndex();
        int Nivel=conNivel.getSelectedIndex();
    try {
        
        Actualizar=ConectarBD.prepareStatement("UPDATE Usuario set NombreUsuario='"+txtNombre.getText()+"',ApellidoUsuario='"+txtApellido.getText()+"',Usuario='"+txtUsuario.getText()+"',Contrase単a='"+txtContra.getText()+"',idTipoUsuario='"+indice+"',idEstadoUsuario='"+estado+"',idAccesoUsuario='"+Nivel+"' where idUsuario='"+txtID.getText()+"'");
        int Contador=Actualizar.executeUpdate();
        if(Contador>0){
            JOptionPane.showMessageDialog(null,"Datos Actualizados Correctamente" );
            VerDatosUsuario();
        }else{
            JOptionPane.showMessageDialog(null,"No selecciono la fila " );
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null,"Error Al Actualizar"+e.toString());
    }
}
